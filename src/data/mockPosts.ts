const hashtag = (value: string) => `#${value.replace(/[^a-z0-9]+/gi, "")}`;

const readableHeight = (heightRange: string) => {
  if (heightRange === "Under 155 cm") return "Petite";
  if (heightRange === "175+ cm") return "Tall";
  return heightRange.replace("–", "-");
};

const createPost = (post: MockPostInput, imageIndex: number, primary = "Similar to you"): Post => ({
  ...post,
  image: fashionImages[imageIndex % fashionImages.length],
  hashtags: [
    hashtag(post.styleTags[0] ?? "Style"),
    hashtag(post.occasionTags[0] ?? "OOTD"),
    hashtag(post.genderStyle[0] ?? "Fashion"),
  ],
  matchLabelPrimary: primary,
  matchLabelSecondary: `${readableHeight(post.heightRange)} · ${post.sizeRange[0]} · ${post.fitTags[0]}`,
});

